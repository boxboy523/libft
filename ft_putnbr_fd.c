#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	digit = n % 10 + '0';
	if (n / 10 != 0)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &digit, 1);
}
